
# Tuple
# Tuples are used to store multiple items in a single variable.
# How to define a tuple , by using rounded/paranthesis like ' ( ) '
# A tuple is a collection which is ordered and unchangeable/Immutable.
t1 = (1,2,3,4,5)
print(t1)
print(type(t1))
# (1, 2, 3, 4, 5)
# <class 'tuple'>

# Tuple is allows duplicate values , unlike set
t2 = (1,2,3,4,1,2,3,'python')
print(t2)
# (1, 2, 3, 4, 1, 2, 3, 'python')

