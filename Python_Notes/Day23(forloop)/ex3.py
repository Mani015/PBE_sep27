
# Print characters from a string that are present at an even index number.


t1 = ('python','java','js','html','css','mangodb','dbms','nodejs')

t2 = len(t1)
print('Total length:',t2)

for i in range(t2):
    print(i,'index of:',t1[i])

# 0 index of: python
# 1 index of: java
# 2 index of: js
# 3 index of: html
# 4 index of: css
# 5 index of: mangodb
# 6 index of: dbms
# 7 index of: nodejs
