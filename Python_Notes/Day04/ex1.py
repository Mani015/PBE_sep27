
# Python divides the operators in the following groups:
#
# Arithmetic operators
# Assignment operators
# Comparison operators
# Logical operators
# Identity operators
# Membership operators
# Bitwise operators

# Arithmetic operators
# ' + '---->plus

a = 10
b = 12
# syntax: operand1 + operand2

print(a + b)
# 22

# ' - ' --> subtraction
print(a-b)
# -2

# ' * ' ---> Asterik
print(b*a)
# 120

print(a*2*b)
# 240

# ' / '--> Division
# Always taking floating value

print(20/10)
# 2.0

print(30/5)
# 6.0


