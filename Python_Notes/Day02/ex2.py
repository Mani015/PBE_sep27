
# Variable Naming Conventions(Identifiers)
#  Variable Naming Conventions:
# • Must start with a letter or an underscore "_".
# • Must consist of a letters, numbers and underscores. It is a case sensitive
python = 'General-purpose'
# Python
_a = 20
A = 30
a = 10

# Eg: First Name, Age, Num1.
# • Cannot be used: 1_hello, @hello, h123#2, -abc.
# • Note: You cannot use reserved words for variable names and identifiers.

