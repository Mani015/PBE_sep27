
# # Comparison operators
# == -->equals to
# !=--> Not equal
# < ---> less than
# > ---> greater than
# >= ---> greater than or equals to
# <= ---> less than or equals to

a = 10
b = 20
# ==: if the both values are equal ,it will return True
# if both values are not equal , it will return False
print(a == b)
# False
print(b == a)
# False
print(a+10 == b)
# True


# != :  if the both values are equal ,it will return False
# # if both values are not equal , it will return True

print('both are not same:',a != b)
# both are not same: True
print('both are not same: ',a != a)
# both are not same:  False


