
# The clear() method removes all the elements from a dictionary.
#
# Syntax
# dictionary.clear()


fruits_prices = {'apple':200,'banana':300,'orange':150,'mango':350,'Graphes':120}
print(fruits_prices)
# before dict : {'apple': 200, 'banana': 300, 'orange': 150, 'mango': 350, 'Graphes': 120}

fruits_prices.clear()

print('After dict:',fruits_prices)
# After dict: {}
